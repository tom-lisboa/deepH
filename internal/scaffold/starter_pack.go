func CreateCoderStarterFile(workspace string, opts AgentTemplateOptions) (string, error) {
	return createAgentFile(workspace, opts, renderCoderStarterTemplate(opts))
}

func CreateReviewerStarterFile(workspace string, opts AgentTemplateOptions) (string, error) {
	return createAgentFile(workspace, opts, renderReviewerStarterTemplate(opts))
}

func CreateReviewSynthStarterFile(workspace string, opts AgentTemplateOptions) (string, error) {
	return createAgentFile(workspace, opts, renderReviewSynthStarterTemplate(opts))
}

func CreateReviewflowCrewFile(workspace string, force bool) (string, error) {
	crewsDir := filepath.Join(workspace, "crews")
	if err := os.MkdirAll(crewsDir, 0o755); err != nil {
		return "", fmt.Errorf("create %s: %w", crewsDir, err)
	}
	outPath := filepath.Join(crewsDir, "reviewflow.yaml")
	if !force {
		if _, err := os.Stat(outPath); err == nil {
			return "", fmt.Errorf("%s already exists (use --force to overwrite)", outPath)
		}
	}
	if err := os.WriteFile(outPath, []byte(reviewflowCrewTemplate), 0o644); err != nil {
		return "", fmt.Errorf("write %s: %w", outPath, err)
	}
	return outPath, nil
}

func renderCoderStarterTemplate(opts AgentTemplateOptions) string {
	model := strings.TrimSpace(opts.Model)
	if model == "" {
		model = "mock-small"
	}
	var b strings.Builder
	b.WriteString("name: coder\n")
	fmt.Fprintf(&b, "description: %s\n", yamlScalar("Starter code editing agent generated by deeph quickstart"))
	if p := strings.TrimSpace(opts.Provider); p != "" {
		fmt.Fprintf(&b, "provider: %s\n", p)
	} else {
		b.WriteString("# provider: your_provider_name  # optional if deeph.yaml has default_provider\n")
	}
	fmt.Fprintf(&b, "model: %s\n", model)
	b.WriteString("system_prompt: |\n")
	b.WriteString("  You are the coder agent for a deepH workspace.\n")
	b.WriteString("  Read only the files needed, keep edits focused, and avoid unnecessary churn.\n")
	b.WriteString("  Prefer line-range reads before writing.\n")
	b.WriteString("  When you change code, keep behavior coherent and summarize changed files plus residual risks.\n")
	b.WriteString("skills:\n")
	b.WriteString("  - file_read_range\n")
	b.WriteString("  - file_write_safe\n")
	b.WriteString("metadata:\n")
	b.WriteString("  tool_max_calls: \"6\"\n")
	b.WriteString("  max_tool_rounds: \"4\"\n")
	b.WriteString("  max_repeated_tool_calls: \"2\"\n")
	b.WriteString("  context_max_input_tokens: \"1200\"\n")
	b.WriteString("  context_moment: \"tool_loop\"\n")
	return b.String()
}

